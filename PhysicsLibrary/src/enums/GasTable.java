package enums;

public enum GasTable{

	AIR(1.29, 1.01), 
	O2(1.43, 0.92), 
	OXYGEN(1.43, 0.92),
	CO2(1.98, 0.82), // Koldioxid
	H2(1.25, 1.04), //Väte
	He(0.18, 5.1) // Helium
	;

	double density;
	double heatCapacity;

	GasTable(double d, double hc) {
		density = d;
		heatCapacity = hc * 1E3; // 1E3 = 1*10^3
	}

}